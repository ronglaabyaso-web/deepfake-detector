def match_patterns(text: str):
    """จับคู่ข้อความกับรูปแบบมิจฉาชีพ (โหมดกฎพื้นฐาน ไม่ต้องมี AI)

    คืน list ของ pattern ที่เจอ พร้อมคำที่จับได้ เรียงจากจำนวนคำที่เจอมากไปน้อย
    """
    if not text:
        return []
    lowered = text.lower()
    matches = []
    for pattern in SCAM_PATTERNS:
        hit_words = [kw for kw in pattern["keywords"] if kw.lower() in lowered]
        if hit_words:
            matches.append({
                "id": pattern["id"],
                "icon": pattern["icon"],
                "name_th": pattern["name_th"],
                "warning_th": pattern["warning_th"],
                "matched_words": hit_words,
            })
    matches.sort(key=lambda m: len(m["matched_words"]), reverse=True)
    return matches


def risk_from_matches(matches) -> int:
    """แปลงจำนวนรูปแบบ/คำที่เจอเป็น % ความเสี่ยงอย่างระมัดระวัง (ไม่ฟันธง 100%)"""
    if not matches:
        return 5
    total_words = sum(len(m["matched_words"]) for m in matches)
    # เจอ 1 คำ ~45%, เพิ่มคำละ ~12% สูงสุด 92% (ไม่มีทางถึง 100 — เป็นสัญญาณเตือนเท่านั้น)
    return min(92, 33 + total_words * 12)
